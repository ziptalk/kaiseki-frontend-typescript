export const stepPrices800 = () => {
  let increment = (0.000053125 - 0.000002125) / (800 - 1);
  let list = [];
  for (let i = 0; i < 800; i++) {
    list.push(ethers.parseEther((0.000002125 + i * increment).toFixed(9)));
  }
  // console.log(list);
  return list;
};
