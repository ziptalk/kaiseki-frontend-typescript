export const createStep = (threshold: number) => {
  const initialPrice = 0.000000000005;
  let list = [];
  const gap = (threshold / 8000000 - 100 * initialPrice) / 4950;
  for (let i = 0; i < 100; i++) {
    list.push(ethers.parseEther((initialPrice + i * gap).toFixed(14)));
  }
  return list;
};
